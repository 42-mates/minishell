#include "minishell.h"

void	welcome_message(void)
{
	ft_putstr_fd(" __  __ _       _     _          _ _ \n", 1);
	ft_putstr_fd("|  \\/  (_)_ __ (_)___| |__   ___| | |\n", 1);
	ft_putstr_fd("| |\\/| | | '_ \\| / __| '_ \\ / _ \\ | |\n", 1);
	ft_putstr_fd("| |  | | | | | | \\__ \\ | | |  __/ | |\n", 1);
	ft_putstr_fd("|_|  |_|_|_| |_|_|___/_| |_|\\___|_|_|\n", 1);
	ft_putstr_fd("\n", 1);
}

bool	is_meta(char c)
{
	return (c == '|' || c == '<' || c == '>');
}
